public class Pato implements Nadador {
    @Override
    public void nadar() {
        System.out.println("El pato nada en el lago.");
    }
    
    public void volar() {
        System.out.println("El pato vuela en el cielo.");
    }
    
    public void clavado() {
        System.out.println("El pato realiza un clavado espectacular.");
    }
}
